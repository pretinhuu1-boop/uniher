function getCampaign(week: number): string | null {
  if (week >= 1 && week <= 4) return 'Janeiro - Novos Hábitos';
  if (week >= 5 && week <= 8) return 'Fevereiro Roxo - Coração e Lúpus';
  if (week >= 9 && week <= 13) return 'Março - Dia Internacional da Mulher';
  if (week >= 14 && week <= 17) return 'Abril Verde - Doação de Órgãos';
  if (week >= 18 && week <= 21) return 'Maio - Dia das Mães';
  if (week >= 22 && week <= 25) return 'Junho Laranja - Leucemia';
  if (week >= 26 && week <= 30) return 'Julho Amarelo - Hepatites';
  if (week >= 31 && week <= 34) return 'Agosto Dourado - Amamentação';
  if (week >= 35 && week <= 39) return 'Setembro Amarelo - Prevenção ao Suicídio';
  if (week >= 40 && week <= 43) return 'Outubro Rosa - Câncer de Mama';
  if (week >= 44 && week <= 47) return 'Novembro - Saúde Integral';
  if (week >= 48 && week <= 52) return 'Dezembro Vermelho - AIDS e Saúde';
  return null;
}

function mkPilula(topic: Topic, theme: string, week: number, day: number, order: number): LessonRow {
  return {
    id: randomUUID(),
    company_id: null,
    title: topic.t,
    description: topic.p.substring(0, 120) + (topic.p.length > 120 ? '...' : ''),
    type: 'pilula',
    theme,
    content_json: JSON.stringify({ tip: topic.p, action: topic.d }),
    xp_reward: 20,
    duration_seconds: 90,
    week_number: week,
    day_of_week: day,
    order_index: order,
    active: 1,
    campaign_context: getCampaign(week),
  };
}


function mkActivity(topic: Topic, theme: string, week: number, day: number, order: number, idx: number): LessonRow {
  const mod = idx % 7;
  let type: string;
  let content: Record<string, unknown>;

  if (mod === 0) {
    // lacuna
    type = 'lacuna';
    const correctText = topic.o[topic.c];
    const words = correctText.split(' ');
    const blankIdx = Math.floor(words.length / 2);
    const keyword = words[blankIdx];
    const withBlank = topic.q.replace(new RegExp('\\?'), '') + ' ___';
    content = {
      sentence: withBlank,
      keyword,
      options: topic.o,
      correct: topic.c,
      explanation: topic.p,
    };
  } else if (mod === 1) {
    // parear
    type = 'parear';
    const shortFact = topic.p.split('.').map(s => s.trim()).find(Boolean) || topic.p;
    const safeOption = (optionIdx: number) => topic.o[optionIdx] || `Item ${optionIdx + 1}`;
    content = {
      instruction: `Combine cada texto com o papel que ele exerce na licao sobre ${topic.t}.`,
      pairs: [
        { term: topic.o[0], definition: topic.o[0] === topic.o[topic.c] ? topic.p.substring(0, 60) : `Opção: ${topic.o[0]}` },
        { term: topic.o[1], definition: `Relacionado a: ${topic.t}` },
        { term: topic.o[2], definition: topic.o[2] === topic.o[topic.c] ? topic.p.substring(0, 60) : `Alternativa: ${topic.o[2]}` },
        { term: topic.o[3], definition: `Conceito de saúde feminina: ${topic.o[3]}` },
      ],
      explanation: topic.p,
    };
    if (Array.isArray((content as any).pairs)) {
      (content as any).pairs = [
        { left: safeOption(0), right: 'Sinal ou fator relacionado ao tema.' },
        { left: safeOption(1), right: `Conecta com: ${topic.t}.` },
        { left: safeOption(2), right: 'Ponto de atenção para o cuidado diário.' },
        { left: safeOption(3), right: 'Contexto prático de saúde feminina.' },
      ];
      (content as any).explanation = shortFact;
    }
  } else if (mod === 2) {
    // ordenar
    type = 'ordenar';
    content = {
      instruction: `Coloque as etapas em ordem correta para ${topic.t.toLowerCase()}`,
      steps: [
        `Comece com: ${topic.d.substring(0, 40)}`,
        `Observe: ${topic.p.substring(0, 40)}`,
        `Pratique: ${topic.r.substring(0, 40)}`,
        `Mantenha: o hábito regularmente`,
      ],
      correctOrder: [0, 1, 2, 3],
      explanation: topic.p,
    };
  } else if (mod === 3) {
    // historia
    type = 'historia';
    content = {
      scenario: `Ana, 35 anos, está aprendendo sobre ${topic.t.toLowerCase()}. Ela percebeu que: "${topic.p.substring(0, 100)}...". Com esse conhecimento, ela se pergunta:`,
      question: topic.q,
      options: topic.o,
      correct: topic.c,
      explanation: topic.p,
    };
  } else if (mod === 4) {
    // flashcard
    type = 'flashcard';
    content = {
      cards: [
        { front: `O que é ${topic.t}?`, back: topic.p.substring(0, 100) },
        { front: topic.q, back: topic.o[topic.c] },
        { front: `Verdadeiro ou falso: ${topic.v}`, back: topic.vb ? 'VERDADEIRO' : 'FALSO' },
        { front: `Desafio do dia sobre ${topic.t}:`, back: topic.d },
      ],
    };
  } else if (mod === 5) {
    // imagem
    type = 'imagem';
    content = {
      question: topic.q,
      options: [
        { emoji: '🔴', label: topic.o[0] },
        { emoji: '🟡', label: topic.o[1] },
        { emoji: '🟢', label: topic.o[2] },
        { emoji: '🔵', label: topic.o[3] },
      ],
      correct: topic.c,
      explanation: topic.p,
    };
  } else {
    // desafio_dia
    type = 'desafio_dia';
    content = {
      challenge: topic.d,
      context: topic.p,
      duration: 'hoje',
      reflection: topic.r,
    };
  }

  const topicTitle = topic.t;
  const shortFact = topic.p.split('.').map((s) => s.trim()).find(Boolean) || topic.p;
  if (type === 'parear') {
    content = {
      instruction: `Relacione cada pergunta ou afirmacao com a resposta que melhor representa o cuidado profissional sobre ${topicTitle}.`,
      question: topic.q,
      options: topic.o,
      correct: topic.c,
      v: topic.v,
      vb: topic.vb,
      reflection: topic.r,
      challenge: topic.d,
      pairs: [
        { left: topic.q, right: topic.o[topic.c] },
        { left: `Verdadeiro ou falso: ${topic.v}`, right: topic.vb ? 'Verdadeiro' : 'Falso' },
        { left: `O que profissionais de saude explicam sobre ${topicTitle.toLowerCase()}?`, right: shortFact },
        { left: topic.r, right: topic.d },
      ],
      explanation: shortFact,
    };
  }

  if (type === 'ordenar') {
    content = {
      instruction: 'Organize a sequencia mais coerente para transformar esse aprendizado em acao.',
      items: [
        `Perceba o tema: ${topicTitle}`,
        `Entenda a ideia principal: ${shortFact}`,
        `Escolha a melhor orientacao: ${topic.o[topic.c]}`,
        `Leve para a rotina: ${topic.d}`,
      ],
      correct_order: [0, 1, 2, 3],
      explanation: 'Primeiro voce reconhece o tema, depois entende a informacao, escolhe a orientacao correta e aplica na pratica.',
    };
  }

  if (type === 'historia') {
    content = {
      scenario: `Voce esta diante de uma situacao relacionada a ${topicTitle.toLowerCase()}. Pensando no que aprendeu, qual resposta faz mais sentido?`,
      choices: topic.o.map((option, idx) => ({
        text: option,
        correct: idx === topic.c,
        feedback: idx === topic.c
          ? `${shortFact} Essa e a resposta mais alinhada com a licao.`
          : `${shortFact} Essa opcao pode parecer possivel, mas nao e a melhor resposta nesse contexto.`,
      })),
    };
  }

  if (type === 'flashcard') {
    content = {
      front: topic.q,
      back: `${topic.o[topic.c]}. ${shortFact}`,
    };
  }

  if (type === 'desafio_dia') {
    content = {
      challenge: topic.d,
      motivation: shortFact,
      tip: topic.r,
    };
  }

  return {
    id: randomUUID(),
    company_id: null,
    title: `${type.charAt(0).toUpperCase() + type.slice(1).replace('_', ' ')}: ${topic.t}`,
    description: `Atividade interativa sobre ${topic.t.toLowerCase()}.`,
    type,
    theme,
    content_json: JSON.stringify(content),
    xp_reward: 20,
    duration_seconds: 90,
    week_number: week,
    day_of_week: day,
    order_index: order,
    active: 1,
    campaign_context: getCampaign(week),
  };
}

// ─── Build all 1050 lessons ──────────────────────────────────────────────────

const themeTopics = [HIDRATACAO, SONO, PREVENCAO, NUTRICAO, MENTAL, CICLO, GERAL];
const themeNames = ['hidratacao', 'sono', 'prevencao', 'nutricao', 'mental', 'ciclo', 'geral'];

interface PendingLesson {
  kind: 'pilula' | 'quiz' | 'vf' | 'reflexao' | 'activity';
  topic: Topic;
  theme: string;
  activityIdx: number;
}

const pending: PendingLesson[] = [];

themeTopics.forEach((topics, ti) => {
  topics.forEach((topic, topicIdx) => {
    const actBase = ti * 30 + topicIdx;
    pending.push({ kind: 'pilula', topic, theme: themeNames[ti], activityIdx: actBase });
    pending.push({ kind: 'quiz', topic, theme: themeNames[ti], activityIdx: actBase });
    pending.push({ kind: 'vf', topic, theme: themeNames[ti], activityIdx: actBase });
    pending.push({ kind: 'reflexao', topic, theme: themeNames[ti], activityIdx: actBase });
    pending.push({ kind: 'activity', topic, theme: themeNames[ti], activityIdx: actBase });
  });
});

// Assign week/day/order — 4 lessons per day slot, 5 days per week, 52 weeks = 1040 slots
// We have 1050 lessons; overflow goes into week 52
const allLessonRows: LessonRow[] = pending.map((p, i) => {
  const daySlot = Math.floor(i / 4);
  const week = Math.min(52, Math.floor(daySlot / 5) + 1);
  const day = (daySlot % 5) + 1;
  const order = i % 4;

  switch (p.kind) {
    case 'pilula':  return mkPilula(p.topic, p.theme, week, day, order);
    case 'quiz':    return mkQuiz(p.topic, p.theme, week, day, order);
    case 'vf':      return mkVF(p.topic, p.theme, week, day, order);
    case 'reflexao': return mkReflexao(p.topic, p.theme, week, day, order);
    case 'activity': return mkActivity(p.topic, p.theme, week, day, order, p.activityIdx);
  }
});

// ─── Seed function ───────────────────────────────────────────────────────────

export function seedGamificationLessons(db: Database.Database): void {
  const existing = db.prepare(
    'SELECT COUNT(*) as count FROM daily_lessons WHERE company_id IS NULL'
  ).get() as { count: number };
  const sampleParear = db.prepare(
    "SELECT content_json FROM daily_lessons WHERE company_id IS NULL AND type = 'parear' LIMIT 1"
  ).get() as { content_json: string } | undefined;
  const parearUpToDate =
    !!sampleParear?.content_json &&
    sampleParear.content_json.includes('"question"') &&
    sampleParear.content_json.includes('"reflection"') &&
    sampleParear.content_json.includes('"challenge"');

  if (existing.count >= 1000 && parearUpToDate) {
    console.log(`[Seed] Already have ${existing.count} global lessons, skipping.`);
    return;
  }

  db.exec(
    'DELETE FROM user_lesson_progress WHERE lesson_id IN (SELECT id FROM daily_lessons WHERE company_id IS NULL)'
  );
  db.exec('DELETE FROM daily_lessons WHERE company_id IS NULL');

  const insert = db.prepare(`
    INSERT INTO daily_lessons
      (id, company_id, title, description, type, theme, content_json,
       xp_reward, duration_seconds, week_number, day_of_week, order_index, active, campaign_context)
    VALUES (?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?)
  `);

  const insertMany = db.transaction((lessons: LessonRow[]) => {
    for (const l of lessons) {
      insert.run(
        l.id, l.company_id, l.title, l.description, l.type, l.theme,
        l.content_json, l.xp_reward, l.duration_seconds,
        l.week_number, l.day_of_week, l.order_index, l.active, l.campaign_context
      );
    }
  });

  insertMany(allLessonRows);
  console.log(`[Seed] Inserted ${allLessonRows.length} lessons.`);
}
